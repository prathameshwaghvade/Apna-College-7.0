// Opeartion that is performed on a single operand..
//Pre increment/post increment
//pre decrement/post decrement
// pre means the value will be changed first
public class Unary {
    public static void main(String[] args) {

        //Pre increment
        int a =10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);
        
        //Post increment
        int x =10;
        int y = x++;
        System.out.println(x);
        System.out.println(y);

        //same goes for the decement.. 
        
    }
}
